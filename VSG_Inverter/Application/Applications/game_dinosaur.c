static void cactus_init(){
    uint8_t i;
    for(i = 0; i < CACTUS_POOL_DEPTH; i++){
        cactus_pool[i].available = 0;
        cactus_pool[i].start_pos = CACTUS_RESPAWN_MIN+ rand()% (CACTUS_RESPAWN_MAX- CACTUS_RESPAWN_MIN);//随机指定生成冷却
    }
}
// 仙人掌的绘制
static void cactus_draw(){
    uint8_t i, j;
    int pos;
    for(i = 0; i < CACTUS_POOL_DEPTH; i++){
        if(cactus_pool[i].available){
            pos = UI_HOR_RES- ((uint32_t)score- cactus_pool[i].start_pos);//显示坐标计算
            Disp_DrawXBMP(pos, DINO_DEFAULT_HEIGHT, cactus_image[cactus_pool[i].type].width, cactus_image[cactus_pool[i].type].height, cactus_image[cactus_pool[i].type].pdata);//绘制图案
        }
    }
}
// 仙人掌的处理
static void cactus_process(){
    uint8_t i, j;
    int pos;
    for(i = 0; i < CACTUS_POOL_DEPTH; i++){
        if(cactus_pool[i].available){
            pos = UI_HOR_RES- ((uint32_t)score- cactus_pool[i].start_pos);//显示坐标计算
            if(pos < -cactus_image[cactus_pool[i].type].width){//标记对象不可用的判断
                cactus_pool[i].available = 0;
                cactus_pool[i].start_pos = CACTUS_RESPAWN_MIN+ rand()% (CACTUS_RESPAWN_MAX- CACTUS_RESPAWN_MIN);//随机指定生成冷却
            }
        }
        else{
            cactus_pool[i].start_pos--;//倒计时
            if(cactus_pool[i].start_pos == 0){//倒计时结束
                cactus_pool[i].available = 1;
                pos = (uint32_t)score;
                for(j = 0; j < CACTUS_POOL_DEPTH; j++)
                    if(j != i && cactus_pool[j].available == 1)
                        if(cactus_pool[j].start_pos+ CACTUS_SPACE > pos)
                            pos = cactus_pool[j].start_pos+ CACTUS_SPACE;
                cactus_pool[i].start_pos = pos;
                //根据分数控制生成类型
                if(score < 500) cactus_pool[i].type = 0; 
                else if(score < 2500) cactus_pool[i].type = rand()% 2; 
                else cactus_pool[i].type = rand()% 4;
            }
        }
    }
}
//仙人掌碰撞检测
uint8_t cactus_collision(int pos_x, int pos_y){
    uint8_t i;
    int pos;
    int dino_pos_x1 = pos_x+ 1;
    int dino_pos_x2 = pos_x+ DINO_W- 1;
    int cactus_pos_x1;
    int cactus_pos_x2;
    for(i = 0; i < CACTUS_POOL_DEPTH; i++){
        if(cactus_pool[i].available){
            pos = UI_HOR_RES- ((uint32_t)score- cactus_pool[i].start_pos);
            cactus_pos_x1 = pos;
            cactus_pos_x2 = pos+ cactus_image[cactus_pool[i].type].width;
            if(dino_pos_x2 > cactus_pos_x1 && dino_pos_x1 < cactus_pos_x2)
                if(pos_y > DINO_DEFAULT_HEIGHT- DINO_H+ 4)
                    return 1;
        }
    }
    return 0;//默认不发生碰撞
}


